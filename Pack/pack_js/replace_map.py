import os
from typing import List, Dict
from dataclasses import dataclass


@dataclass
class ReplaceMapModel:
    old_str: str
    new_str: str


class ReplaceMap:
    
    def __init__(self, folder: str = None):
        self.folder = folder or os.getcwd()
        self._built_in_maps = self._get_built_in_maps()
    
    def get_replace_maps(self) -> List[ReplaceMapModel]:
        custom_maps = self._get_custom_maps()
        all_maps = custom_maps + self._built_in_maps
        
        result = []
        index = 0
        for map_item in all_maps:
            if not map_item.old_str.strip():
                break
            
            if map_item.new_str:
                result.append(map_item)
            else:
                new_str = f"_0x{index}"
                result.append(ReplaceMapModel(map_item.old_str, new_str))
                index += 1
        
        return result
    
    def _get_custom_maps(self) -> List[ReplaceMapModel]:
        config_file = os.path.join(self.folder, 'definedMap.txt')
        print(config_file)
        if not os.path.exists(config_file):
            return []
        
        result = []
        try:
            with open(config_file, 'r', encoding='utf-8') as f:
                for line in f:
                    line = line.strip()
                    if not line:
                        continue
                    
                    parts = line.split('|')
                    if len(parts) >= 2:
                        result.append(ReplaceMapModel(parts[0], parts[1]))
        except Exception as e:
            print(f"Error reading custom maps: {e}")
        
        return result
    
